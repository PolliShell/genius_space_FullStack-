function findPath(mountain) {
    // Копіюємо останній рядок гори
    let dp = mountain[mountain.length - 1].slice();

    // Починаємо обчислення з пенсульного рядку і рухаємось вгору
    for (let i = mountain.length - 2; i >= 0; i--) {
        for (let j = 0; j <= i; j++) {
            // Вибираємо більше значення з двох сусідніх значень у наступному рядку та додаємо до поточного значення
            dp[j] = mountain[i][j] + Math.max(dp[j], dp[j + 1]);
        }
    }

    // dp[0] містить максимальну суму після завершення обчислень
    return dp[0];
}

// Приклад вхідних даних
let mountain = [[6], [7, 10], [12, 11, 9], [90, 25, 13, 14]];

// Вивід результату
console.log(findPath(mountain)); // Виведе: 115
